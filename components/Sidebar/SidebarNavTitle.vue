<template>
  <li :class="classList">
    <template v-if="wrapper && wrapper.element">
      <component
        v-bind="wrapper.attributes"
        :is="wrapper.element"
      >
        {{ name }}
      </component>
    </template>
    <template v-else>
      {{ name }}
    </template>
  </li>
</template>
<script>
export default {
  name: 'SidebarNavTitle',
  props: {
    name: {
      type: String,
      default: ''
    },
    classes: {
      type: String,
      default: ''
    },
    wrapper: {
      type: Object,
      default: () => { }
    }
  },
  computed: {
    classList() {
      return ['nav-title', ...this.itemClass]
    },
    itemClass() {
      return this.classes ? this.classes.split(' ') : ''
    }
  }
}
</script>
